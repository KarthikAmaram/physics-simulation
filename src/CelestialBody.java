public class CelestialBody {
    public double x, y, vx, vy, mass, radius;

    public CelestialBody(double x, double y, double vx, double vy, double mass, double radius) {
        this.x = x;
        this.y = y;
        this.vx = vx;
        this.vy = vy;
        this.mass = mass;
        this.radius = radius;
    }
}
